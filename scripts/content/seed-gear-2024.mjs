async function main() {
  const cards = await fetchAll('/api/cards', 'cards');
  const have = new Set();
  for (const c of cards) {
    have.add(norm(c.name));
    // без магических суффиксов
    have.add(norm(c.name.replace(/\s*\+\d+$/, '')));
  }
  const missing = ITEMS.filter(([name, , , , , , aliases]) => {
    if (have.has(norm(name))) return false;
    const short = name.replace(/\s*\(\d+ шт\.\)$/, '');
    if (have.has(norm(short))) return false;
    return !aliases.some((a) => have.has(norm(a)));
  });
  console.log(`Позиций в эталоне: ${ITEMS.length}; отсутствует: ${missing.length}; режим: ${APPLY ? 'APPLY' : 'dry-run'}`);
  const token = APPLY ? await login() : null;
  const failed = [];
  for (const [name, price, currency, weight, type, description] of missing) {
    console.log(' +', name);
    if (!APPLY) continue;
    const payload = {
      name, description, rarity: 'common',
      price, price_currency: currency,
      ...(weight != null ? { weight } : {}),
      ...(type ? { type } : {}),
      source: 'PHB 2024',
    };
    try {
      await apiRequest(token, 'POST', '/api/cards', payload);
    } catch (e) {
      failed.push(`${name}: ${String(e).slice(0, 120)}`);
    }
  }
  if (failed.length) console.log('Ошибки:', failed);
  console.log('Готово.');
}

main().catch((e) => { console.error(e); process.exit(1); });
